bool isPossible(ll k,vi &a){
    bool flag=true;
    ll i=a.size()-1;
    for(ll j=k-1;j>=0;j--,i--){
        if(!flag) break;
        flag = (a[i]>a[j])&&(a[i-1]>a[j]) && (i>k);
    }
    return flag;
}
